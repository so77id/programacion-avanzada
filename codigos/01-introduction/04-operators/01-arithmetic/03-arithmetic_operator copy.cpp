#include <iostream>
using namespace std;

int main() {
    int e1 = 100;
    int e2 = -100;
    int e3 = 3;

    cout << "e1 + e2: " << (e1 + e2) << endl;
    cout << "e1 - e2: " << (e1 - e2) << endl;
    cout << "e1 * e3: " << (e3 * e1) << endl;
    cout << "e2 / e3: " << (e2 / e3) << endl;
    cout << "e1 % e3: " << (e1 % e3) << endl;
    cout << "e2 % e3: " << (e2 % e3) << endl;
}